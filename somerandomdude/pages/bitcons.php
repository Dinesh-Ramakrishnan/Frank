<div class='four columns'>
<h3>Small, yet legible</h3>
<p>The icons are extremely small &#8212; with the actual marks varying between 8x8 to 12x12 pixels. Due to being pixel icons, the marks stay legible even at the small sizes &#8212; allowing for low-profile aesthetics.</p> 
<h3>3 different sizes</h3>
<p>As well as coming in many colors, the  icons also come in 16x16, 32x32 &amp; 64x64 pixels. The 32x32 and 64x64 icons have a chunkier look, maintaing the pixel style.</p>
</div>
<div class='four columns'>
<h3>10 different colors</h3>
<p>All  icons come in a wide variety of colors (10 in all) in order for greater convenience. The colors hit all the major hues in the spectrum as well as many colors used in the scheme on this site.</p>
<h3>Small, yet legible</h3>
<p>The icons are extremely small — with the actual marks varying between 8×8 to 12×12 pixels. Due to being pixel icons, the marks stay legible even at the small sizes — allowing for low-profile aesthetics.</p>
</div>
