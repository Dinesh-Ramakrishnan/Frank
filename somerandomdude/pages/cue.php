<section id='info'>
<h1>A different approach towards gestural icons</h1>
<p class="large">Cue is intended to be a foundational set of icons to build a standard visual language of touch-based interactions. Each gesture is distilled to its core action to exhibit a more figurative, iconic aesthetic.</p>
<div id='features' class='row'>
<div class='four columns'>
<h3>Abstracted representation</h3>
<p>Not everyone is right handed, nor do they perform gestures uniformly which makes literal expression less than optimal.</p>
<h3>A system to grow from</h3>
<p>Gestures will come, go and change over time. Cue was designed to support that. <a href='/2011/10/10/redesigning-gesture-icons-a-proposed-system/'>Learn more about the thinking.</a></p>
</div>
<div class='four columns'>
<h3>Legible at small sizes</h3>
<p>Mobile devices are already space-constrained. My goal was to create icons that could take up little space in a mobile interface if needed.</p>
<h3>Multiple formats</h3>
<p>Cue comes in PNG (4 sizes), SVG, Omnigraffle and InDesign formats. In addition, they icons come with and without labels.</p>
</div>
