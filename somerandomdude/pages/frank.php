<div id='features' class='row'>
<h3>Frank is fast</h3>
<div class='feature row'>
<div class='six columns'>
<p>Frank's main focus is speed. The parent theme's default home page makes 9 database queries and consists of 2 requests weighing ~29Kb (9.6Kb gzipped). Frank keeps it basic—no Javascript frameworks, no unecessary images, just a simple, no-frills, screaming fast blog.</p>
</div>
<div class='six columns'>
<img src='/wp-content/images/pages/frank/fast.png' />
</div>
</div>

<h3>Frank is flexible</h3>
<div class='feature row'>
<div class='six columns'>
<p>Frank has several different layouts to choose from for your home page. Most layouts let you specify the number of posts to show and from what categories. Templates can be stacked by any amount and order to give you the structure you need. Additionally, there are plenty of well-placed widgets that will give you that extra level of customization.</p>
</div>
<div class='six columns'>
<img src='/wp-content/images/pages/frank/flexible.png' />
</div>
</div>

<h3>Frank is forward-thinking</h3>
<div class='feature row'>
<div class='six columns'>
<p>Frank is built on HTML5 and CSS3. The theme strives for 100% valid HTML5 templates. One way Frank is so small is that it uses CSS3's effects and transitions to avoid unneeded images and Javascript. One side effect of this approach is that Frank does not support Internet Explorer 6 and *moderately* supports Internet Explorer 7 & 8. </p>
</div>
<div class='six columns'>
<img src='/wp-content/images/pages/frank/forward.png' />
</div>
</div>

<h3>Frank fits on your phone</h3>
<div class='feature row'>
<div class='six columns'>
<p>Frank uses a subset of <a href='http://foundation.zurb.com/'>Foundation</a> to provide a responsive layout for desktops, tablets and phones. Add this to the theme's small footprint and you have a mobile-optimized blog.</p>
</div>
<div class='six columns'>
<img src='/wp-content/images/pages/frank/phone.png' />
</div>
</div>

