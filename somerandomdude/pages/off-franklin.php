<div class='four columns'>
<h3>Content friendly</h3>
<p>The interface was designed to work well with all forms of Tumblr content. The interface is intended to simply stay out of the way on a post's individual page. Posts with content small or large work just fine.</p>
<h3>Easy customization</h3>
<p>The theme opted for a simple white-level color system as opposed to content-specific colors. This should allow for a simpler and more reliable color scheme. Just choose your highlight, light, dark, shadow, black and accent colors.</p>
</div>
<div class='four columns'>
<h3>Efficient index layout</h3>
<p>Off Franklin's front page uses the wonderful <a href="http://masonry.desandro.com/">Masonry jQuery plugin</a>, which allows for an efficient use of space. Long-form content in posts are truncated for uniformity &ndash; ensuring a consistent experience regardless of content.</p>
<h3>Full Tumblr Support</h3>
<p>The April 19, 2010 release now supports Tumblr's page feature. In addition, you can now toggle the sticky footer on and off  for those using <a href='http://www.streampad.com/'>Streampad</a> or any other service which needs that space.</p>
</div>
<div class='four columns'>
<h3>Off Franklin loves <a href="/work/iconic/">Iconic</a></h3>
<p>The theme was designed specifically to work with the icon set. Make sure to <a href="/work/iconic/">download the set</a> if you are itching to customize the theme for your needs. The theme also has a secret crush on <a href="/work/sanscons/">Sanscons</a>.</p>
